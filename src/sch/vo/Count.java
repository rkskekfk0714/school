package sch.vo;

public class Count {
	private String id;
	private String pwd;
	private int list;
	public Count() {}
	public String getId() {
		return id;
	}
	public void setId(String tId) {
		this.id = tId;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String tPwd) {
		this.pwd = tPwd;
	}
	public int getList() {
		return list;
	}
	public void setList(int tList) {
		this.list = tList;
	}
	
}
